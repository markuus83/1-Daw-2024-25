
			-------------------------------------------------------------- 5. Tarefa de Consultas Subconsultas --------------------------------------------------------------

--Proposta 1. Nome de todos os fabricantes dos que hai produtos na BD. Non se permite usar combinacións nesta consulta.




--Proposta 2. Nome de todos os fabricantes dos que non hai produtos na BD. Non se permite usar combinacións nesta consulta.




--Proposta 3. Número de pedido, cantidade e data de pedido para aqueles pedidos recibidos nos días en que un novo empregado foi contratado. Non se permite usar combinacións nesta consulta.




--Proposta 4. Cidade e obxectivo das sucursais cuxo obxectivo supera a media das cotas de todos os vendedores da BD. Non se permite usar combinacións nesta consulta.




--Proposta 5. Número de empregado e cantidade media dos pedidos daqueles empregados cuxa cantidade media de pedido é superior á cantidade media global (de todos os pedidos).
--Non se permite usar combinacións nesta consulta.




--Proposta 6. Nome dos clientes que aínda non fixeron pedidos. Non se permite usar combinacións nesta consulta.




--Proposta 7. Nome completo dos empregados cuxas cotas son iguais ou superiores ao obxectivo da sucursal da cidade de Vigo. Ten en conta que se a cota dun vendedor (empregado) é nula debemos considerala como un 0,
--e do mesmo xeito actuaremos co obxectivo da sucursal. Non se permite usar combinacións nesta consulta.




--Proposta 8. Nome dos produtos para os que existe polo menos un pedido que ten unha cantidade de polo menos 20 unidades. Hai que lembrar que a identificación dun produto faise pola combinación do 
--código do fabricante e o do produto. A solución deberá facerse empregando o predicado EXISTS cunha subconsulta correlacionada. Non se permite usar combinacións.




--Proposta 9. Cidades das sucursais onde exista algún empregado cuxa cota de vendas represente máis do 80% do obxectivo da oficina onde traballa. Para resolver esta consulta deberase empregar unha
--subconsulta correlacionada precedida de ANY.




--Proposta 10. Nome dos clientes cuxos empregados asignados traballan en sucursais da rexión OESTE. Non se poden usar joins, só subconsultas encadeadas.