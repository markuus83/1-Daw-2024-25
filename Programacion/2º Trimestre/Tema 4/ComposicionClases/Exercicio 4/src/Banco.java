public class Banco {
    
    //Atributos
    private String nome;
    private String pais;


    public Banco(){
        this.setNome(nome);
        this.setPais(pais);
    }


    //GETTERS & SETTERS
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getPais() {
        return pais;
    }
    public void setPais(String pais) {
        this.pais = pais;
    }

    
}
